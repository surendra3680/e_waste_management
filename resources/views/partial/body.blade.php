                    <p>The Ministry of Environment, Forests, and Climate Change (MoEFCC) is primarily responsible
                        for regulations regarding electronic waste. Additionally, the Central Pollution Control
                        Board (CPCB) and State Pollution Control Board (SPCB) produce implementation procedures to
                        ensure proper management of rules set forth by the MoEFCC..</p>
                    <b>Amendment to the E-Waste Management Rules, 2018</b>
                    <p>This amendment relaxes certain aspects of the strict E- Waste (Management Rules of 2016).
                        Specifically, the amendment focusses on the e-waste collection targets by 10% during
                        2017-2018, 20% during 2018-2019, 30% during 2019-2020, and so on. This amendment also gives
                        the Central Pollution Control Board power to randomly select electronic equipment on the
                        market to test for compliance of rules. The financial cost associated with this testing
                        shall be the responsibility of the government, whereas previously, this responsibility was
                        of the producer.</p>

